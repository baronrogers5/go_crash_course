package strutil

// Reverse This function returns the reverse of a passed dtring
// params: s string -- The string to reverse
// return: string -- The reversed string
func Reverse(s string) string {
	runes := []rune(s)
	for i, j := 0, len(runes)-1; i < j; i, j = i+1, j-1 {
		runes[i], runes[j] = runes[j], runes[i]
	}

	return string(runes)
}
